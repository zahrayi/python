# find root for y=ax**2+bx+c
a=1
b=-4
c=4
d= (b ** 2 - 4 * a * c)
r1= (- b + (d**0.5)) / (2 * a)
r2= (- b - (d**0.5)) / (2 * a)
print("first root:" , r1 , "second root:" , r2)
